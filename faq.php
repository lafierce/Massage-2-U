<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">What is an outcall massage?</span><br />
An outcall massage is when a massage therapist comes to your location.  
Please provide the address of your physical location when booking a massage.
</p>

<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">How do I know my credit card information is safe?</span><br />
<span class="m2uEm smallText">Massage 2 U</span> is accredited by the BBB, the leader in trust and reliability. 
Your credit cards are not handled by the massage therapists. You are protected by several layers of security. 
If you do not want to disclose your credit card information you can pay by e-bill.  
Your e-bill is processed by PayPal, the most secure name in online payments.  A PayPal account is not required.  
</p>

<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">How do I cancel my appointment and get a refund?</span><br />
A link to cancel or change your appointment is sent in your appointment confirmation email.  
It is the responsibility of the client to cancel their appointments. However, You can also call us at 888.308.8965.  
Please call to reschedule appointments to avoid accidental duplicate charges.  
You can cancel at anytime up to 2 hours before the appointment and receive a full refund.

</p>

<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">What should I expect for my first appointment?</span><br />
Please allow your massage therapist a +/- 15 minutes grace period for arrival.  
All first time clients will be required to fill out a confidential client intake form. 
There will be an initial consultation upon the massage therapist's arrival.  
This will take no more than 15 minutes and will not take away from your “hands on” massage time.
</p>

<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">When will my credit card be charged?</span><br />
Payment is collected at the time of your appointment. A receipt will be sent to your email address 
immediately after your payment. You can cancel at anytime up to 2 hours before the appointment 
and receive a full refund.  If you choose to pay by e-bill your payment is due before your therapist 
leaves for your appointment.
</p>

<p><span class="large_underline" style="font-size: 10pt; font-weight: bold;">Do I tip my massage therapist?</span><br />
Tipping is not available on credit cards or prepayments.  Gratuity is not included.  However, tips are greatly appreciated.
</p>

